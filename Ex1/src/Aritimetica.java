package Ex1.src;

public class Aritimetica {
    
    public static void soma(int a, int b){
        int resultado = a + b;
        System.out.println("soma = " + resultado);
    }

    public static void subtacao(int a, int b){
        int resultado = a - b;
        System.out.println("subtração = " + resultado);
    }

    public static void multiplicacao(int a, int b){
        int resultado = a * b;
        System.out.println("multiplicacao = " + resultado);
    }

    public static void divisao(int a, int b){
        int resultado = a / b;
        System.out.println("divisao = " + resultado);
    }

}
